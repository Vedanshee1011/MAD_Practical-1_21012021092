fun main(){
    var num1:Int=10
    val num2:Double=num1.toDouble()
    println("$num1:Integer value:$num1")
    println("Integer value(from integer):$num2")
    var a:String = "10"
    val b:String=a.toString()
    println("$a:String value:$a")
    println("Integer value1(from String):$b")
    var c:String="11.12"
    val d:String=c.toString()
    println("$c:String value:$c")
    println("double value(from String):$d")
}